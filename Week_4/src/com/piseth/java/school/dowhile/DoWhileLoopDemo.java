package com.piseth.java.school.dowhile;

public class DoWhileLoopDemo {

	public static void main(String[] args) {
		/*
		 * do{
		 * statements
		 * 
		 * }while(testExpression);
		 */
		// display number from 1 to 5
		int i = 1;
		do {
			System.out.println(i);
			i++;
		}while(i <= 5);
		
		
	}

}
